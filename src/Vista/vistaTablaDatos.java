    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        int confirm = JOptionPane.showConfirmDialog(rootPane,"Eliminar√° un registro","Mensaje de Advertencia",2);
        
        if(confirm==0){
            Data dt = new Data();
            ModeloData md = new ModeloData();
            
            dt.setIdRegistro(Integer.parseInt(txtIdRegistro.getText()));
            try {
                md.Eliminar(dt);
            } catch (SQLException ex) {
                Logger.getLogger(vistaTablaDatos.class.getName()).log(Level.SEVERE, null, ex);
            }
            mostrar();
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        btnEliminar.setEnabled(true);
        int fila = tblData.rowAtPoint(evt.getPoint());
        
        txtIdRegistro.setText(tblData.getValueAt(fila,0).toString()); 
        
       
        
        
    }//GEN-LAST:event_tblDataMouseClicked

